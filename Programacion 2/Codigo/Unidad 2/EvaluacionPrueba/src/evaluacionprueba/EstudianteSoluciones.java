
package evaluacionprueba;


public class EstudianteSoluciones {

private Estudiante estudiante;
private Soluciones soluciones;

    public EstudianteSoluciones(Estudiante estudiante, Soluciones soluciones) {
        this.estudiante = estudiante;
        this.soluciones = soluciones;
    }

    public Estudiante getEstudiante() {
        return estudiante;
    }

    public void setEstudiante(Estudiante estudiante) {
        this.estudiante = estudiante;
    }

    public Soluciones getSoluciones() {
        return soluciones;
    }

    public void setSoluciones(Soluciones soluciones) {
        this.soluciones = soluciones;
    }

   
    
}
